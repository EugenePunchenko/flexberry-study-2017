CREATE TABLE SPORTSMAN (
ID_SPORTSMAN INT NOT NULL PRIMARY KEY,
FIRSTNAME NVARCHAR(50) NOT NULL,
LASTNAME NVARCHAR(50) NOT NULL,
PATRONYMIC NVARCHAR(50) NOT NULL,
GROUPNAME NVARCHAR(50) NOT NULL,
DATE_BIRTH DATE NOT NULL
)

CREATE TABLE ZONE (
ID_ZONE INT NOT NULL PRIMARY KEY,
MIN_PULSE INT NOT NULL,
MAX_PULSE INT NOT NULL,
LOAD_POINTS INT NOT NULL
)

CREATE TABLE EXERCISE (
ID_EXERCISE INT NOT NULL PRIMARY KEY,
NAME NVARCHAR(50) NOT NULL,
ID_ZONE INT NOT NULL REFERENCES ZONE(ID_ZONE)
)

CREATE TABLE TRAINING (
ID_TRAINING INT NOT NULL PRIMARY KEY,
TRAINING_DATE DATE NOT NULL,
ID_SPORTSMAN INT NOT NULL REFERENCES SPORTSMAN(ID_SPORTSMAN),
TRAINING_LOAD DECIMAL,
IS_EXIST BIT NOT NULL,
)

CREATE TABLE TRAINING_EXERCISES (
ID_TRAINING_EXERCISES INT NOT NULL PRIMARY KEY,
ID_TRAINING INT NOT NULL REFERENCES TRAINING(ID_TRAINING),
ID_EXERCISE INT NOT NULL REFERENCES EXERCISE(ID_EXERCISE),
EXERCISE_TIME TIME NOT NULL,
REPEATS INT NOT NULL,
AVG_PULSE DECIMAL NOT NULL
);
